let x=5;
let y=8;
 //addition
 console.log('x+y=',x+y);
 //subtraction
 console.log('x-y=',x-y);
 //multiplication
console.log('x*y=', x*y);
//division
console.log('x/y=', x/y);

 //remainder
console.log('x%y=', x%y);

 // increment
console.log('++x = ', ++x); 
console.log('x++ = ', x++); 
console.log('x = ', x);     
// decrement
 console.log('--y = ', --y); 
 console.log('y-- = ', y--); 
 console.log('y = ', y);     

 //exponentiation
 console.log('x**y = ',x**y);

// equal operator
console.log(2 == 2); 
console.log(2 == '2'); 

 // not equal operator
 console.log(3 != 2); 
 console.log('hello' != 'Hello');
 console.log(2 != '2'); 


// strict equal operator
 console.log(2 === 2); 
 console.log(2 === '2'); 

//strict not equal operator
 console.log(2 !== '2'); 
 console.log(2 !== 2); 

 //comparsion
 const a = 3, b = 2;
 console.log(a > b);


//Logical AND
const c= 5, d = 3;
console.log(c < 6) && (d < 5); 